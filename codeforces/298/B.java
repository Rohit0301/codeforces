    public static void main(String args[]) throws IOException
    {
      Scanner sc=new Scanner(System.in);
      int t=sc.nextInt();
      int sx=sc.nextInt();
      int sy=sc.nextInt();
      int ex=sc.nextInt();
      int ey=sc.nextInt();
      String s=sc.next();
      int time=-1;
      for(int i=0; i<t; i++){
        switch(s.charAt(i)){
            case 'E':
                if(sx<ex){
                    sx+=1;
                }
                break;
            case 'S':
                if(sy>ey){
                    sy-=1;
                }
                break;
            case 'W':
                if(sx>ex){
                    sx-=1;
                }
                break;
            case 'N':
                if(sy<ey){
                    sy+=1;
                }
                break;
            default:
                break;
        }
 
        if(sx == ex && sy == ey){
            time = i+1;
            break;
        }
    }
    System.out.println(time);

      
}
