import java.util.*;
import java.io.*;
import java.math.*;
public class cf{
	public static void main(String[] args) throws IOException{
		Scanner sc=new Scanner(System.in);
		  
		    int t=sc.nextInt();
		    while(t-->0){
		      String s=sc.next();
          int n=s.length();
          String ans="";
          boolean f=true;  
		      for(int i=0;i<n;i++){
		          if(f){
		          	if(s.charAt(i)!='a'){
		          		ans+="a";
		          	}
		          	else{
		          		ans+="b";
		          	}
              f=!f;
		          }
		          else{
		          	if(s.charAt(i)=='z'){
		          		ans+="y";
		          	}
		          	else{
		          		ans+="z";
		          	}
		          	f=!f;
		          }
		      }
		      System.out.println(ans);
		    }
	}
}



