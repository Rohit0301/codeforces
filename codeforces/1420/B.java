    public static void main(String args[]) throws IOException
    {
      Scanner sc=new Scanner(System.in);
     
     int t=sc.nextInt();
     while(t-->0){
        int n=sc.nextInt();
        long a[]=new long[n];
        for(int i=0;i<n;i++){
          a[i]=sc.nextLong();   
        }

        HashMap<Long,Long> map=new HashMap<>(); 
        for(int i=0;i<n;i++){
          long x=a[i];
          long j=0;
          long ans=0;
          while(x>0){
            if(x%2!=0)
              ans=j;
            j++;
            x=x>>1;
          }   

          if(map.containsKey(ans)){
            map.put(ans,map.get(ans)+1);
          }   
          else{
            map.put(ans,1l);
          }
        }  

         long res=0;
      for(Map.Entry<Long,Long> mp:map.entrySet()){
                    
              long g=mp.getValue();
            res=res+(g*(g-1)/2);
        }

        System.out.println(res);
     }


      
}
