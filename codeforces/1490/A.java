import java.util.*;

public class cf {
	public static void main(String[] args) {
	 Scanner sc=new Scanner(System.in);
	   int t=sc.nextInt();
	   while(t-->0){
	     int n=sc.nextInt();
	     int a[]=new int[n];
	     for(int i=0;i<n;i++){
	       a[i]=sc.nextInt();   
	     }
         long c=0;
	     for(int i=0;i<n-1;i++){
	         int x=a[i];
	         if(x<=a[i+1]){
	         	while(((double)a[i+1]/x)>2){
                   c++;
                   x*=2;
	         	}
	         }
	         else{
	         	int y=a[i+1];
	         	while(((double)a[i]/y)>2){
	         		y*=2;
	         		c++;
	         	}
	         }
	     }
	     System.out.println(c);
	   }
		  
	}
}