public class cf {
	public static void main(String[] args) {
		Scanner sc=new Scanner(System.in);
		  int t=sc.nextInt();
		  while(t-->0){
		    long n=sc.nextLong();
		    long k=sc.nextLong();
		    long bpos=0;
		    if(n%2==0){
		    	bpos=(k)%(n);
		    	if(bpos==0)
		    		bpos=n;
              System.out.println(bpos);
		  }
		  else{
		     long res;
		     long mid=n/2;
		     long ans=(k%(mid*n))==0?(mid*n):(k%(mid*n));
		     res=ans%mid==0?(ans/mid)-1:ans/mid;
		     ans=ans%mid==0?mid:ans%mid;
		     long tota=ans+res+(res*mid);
		     tota=tota%n==0?n:tota%n;
		     System.out.println(tota);
          }


		  }
	}
}