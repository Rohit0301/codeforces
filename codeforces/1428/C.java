  static int dp[][];
    static int solve(String s1,String s2,int n,int m){
      if(n<=0 || m<=0)
        return 0;
      if(dp[n][m]!=-1)
        return dp[n][m];
      if(s1.charAt(n-1)==s2.charAt(m-1)){
        return dp[n][m]=solve(s1,s2,n-1,m-1)+2;
      }
      else{
        return dp[n][m]= Math.max(solve(s1,s2,n-1,m)-1,solve(s1,s2,n,m-1)-1);
      }
    }
    
   
